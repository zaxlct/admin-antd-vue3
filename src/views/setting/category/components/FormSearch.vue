<template>
  <a-card class="mb15">
    <div class="__table_form_search_component">
      <form-create
        v-model:api="fApi"
        v-model="data"
        :option
        :rule
      >
        <template #type-btns>
          <section
            class="flex mb15"
            style="width: 100%;"
          >
            <AButton
              @click="submitForm"
              type="primary"
              class="mr10"
            >查询</AButton>
            <AButton
              class="mr10"
              @click="resetForm"
            >重置</AButton>
            <div class="flex1 flex_end">
              <AButton
                type="primary"
                @click="emit('addItem')"
              >添加分类</AButton>
            </div>
          </section>
        </template>
      </form-create>
    </div>
  </a-card>
</template>

<script setup>
const props = defineProps({
  isParent: { // 是否为一级分类
    type: Boolean,
    default: true,
  },
  parentId: {
    type: Number,
    default: null,
  },
})
const params = defineModel()
const data = reactive({
  name: '',
  create_time: [],
  parentId: null,
})
const fApi = ref({})

const emit = defineEmits(['addItem', 'search'])

watch(() => props.parentId, val => {
  if (val) {
    data.parentId = val
  }
}, {
  immediate: true,
})

const option = {
  resetBtn: false,
  submitBtn: false,
  global: {
    '*': {
      col: {
        show: false,
      },
      wrap: {
        labelCol: { span: 8 },
      },
    },
  },
}

const rule = ref([
  {
    type: 'input',
    field: 'name',
    title: '分类名称',
    value: '',
  },
  {
    type: 'rangePicker',
    field: 'create_time',
    title: '创建时间',
    value: '',
    props: {
      format: 'YYYY-MM-DD',
      valueFormat: 'X',
    },
  },
  { type: 'btns' },
])

if (!props.isParent) {
  rule.value.unshift({
    type: 'select',
    field: 'parentId',
    title: '一级分类',
    value: '',
    props: {
      allowClear: true,
    },
    options: [],
    effect: {
      fetch: {
        action: '/api/v1/setting/live/category',
        data: {
          page: 1,
          limit: 99,
          level: 1,
        },
        to: 'options',
        method: 'get',
        parse: res => res.items.map(item => ({ value: item.category_id, label: item.name })),
      },
    },
  })
}

function resetForm() {
  fApi.value.resetFields()
  getData(data)
}

function submitForm() {
  fApi.value.submit(formData => {
    getData(formData)
  })
}

function getData(data) {
  params.value = {
    ...data,
    create_time: data.create_time ? data.create_time?.join(',') : undefined,
  }
}

defineExpose({
  resetForm,
})
</script>

